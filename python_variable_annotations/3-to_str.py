#!/usr/bin/env python3
"""
This module defines a function `to_str` that converts a float to its string
representation.
3-to_str.py
"""


def to_str(n: float) -> str:
    """
    Convert a float to its string representation.

    Args:
        n (float): The input float.

    Returns:
        str: The string representation of n.
    """
    return str(n)
